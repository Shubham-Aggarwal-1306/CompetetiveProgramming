class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int num = N;
        int count = 0;
        while (N>0){
            int curr = N%10;
            if (curr != 0) { 
            if (num%curr == 0) count++;
            }
            N = N/10;
        }
        return count;
    }
};

